public class MathOperation {

  public static void main(String[] args) {
    int bagOfSweets1 = 5;
    int bagOfSweets2 = 10;

    long starsInMilkyWay = 2500000000L;
    long starsInAndromedia = 10000000000L;

    double testScore = 6.7;
    double bonusMarks = 2.5;

    System.out.println(bagOfSweets2 + bagOfSweets1);
    System.out.println(starsInAndromedia + starsInMilkyWay);
    System.out.println(testScore + bonusMarks);

    System.out.println(bagOfSweets2 - bagOfSweets1);
    System.out.println(starsInAndromedia - starsInMilkyWay);
    System.out.println(testScore - bonusMarks);

    System.out.println(bagOfSweets2 * bagOfSweets1);
    System.out.println(starsInAndromedia * starsInMilkyWay);
    System.out.println(testScore * bonusMarks);

    System.out.println(bagOfSweets2 / bagOfSweets1);
    System.out.println(starsInAndromedia / starsInMilkyWay);
    System.out.println(testScore / bonusMarks);

    System.out.println(5 % 2);
    System.out.println(2 % 5);

    int counter = 0;
    System.out.println(counter++);
    System.out.println(counter--);

    System.out.println(counter += 10);
    System.out.println(counter -= 10);
  }
}
